package util;

public class Aluno {
	private String nome;
	private String serie;
	private double n1;
	private double n2;
	private double media;
	
	public Aluno(String nome, String serie, double n1, double n2, double media) {
		this.nome = nome;
		this.serie = serie;
		this.n1 = n1;
		this.n2 = n2;
		this.media = media;
	}
}
